#Função PRINT, dentro dos parenteses, passamos o que chamamos de ARGUMENTOS:
print(12,34, sep="-") #argumento SEP é usado como separador dos argumentos impressos na função PRINT
print(56,78, sep='-') #em SEP podemos usar aspas duplas ou aspas simples. Por padrão, o separador vem em branco

# \r \n -> CRLF 0 Carriage return and Line feed
print(12,34, 1011, sep="-", end='\n#') #argumento END define o que virá no final do argumento impresso
print(56,78, sep="-", end='\n') 
print(9,10, sep="-", end='\n') 

